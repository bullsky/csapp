/*
 * isTmax - returns 1 if x is the maximum, two's complement number,
 *     and 0 otherwise 
 *   Legal ops: ! ~ & ^ | +
 *   Max ops: 10
 *   Rating: 1
 */
int isTmax(int x) {
  // x==max/x==0xffffff时，~x^(x+1)为0x0000000
  // !!(~x)去掉x==0xfffffff的情况
  int ret = !!(~x)&!(~x^(x+1));
  return ret;
}
